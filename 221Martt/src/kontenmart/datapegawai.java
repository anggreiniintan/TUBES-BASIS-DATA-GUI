    private void tampil(){
        
        DefaultTableModel modell= new DefaultTableModel();
            modell.addColumn("No");
            modell.addColumn("ID");
            modell.addColumn("Nama");
            modell.addColumn("Alamat");
            modell.addColumn("Gender");
            modell.addColumn("No HP");
            modell.addColumn("Password");
        try{
            String sql = "SELECT * FROM pegawai";
            st=con.createStatement();
            rs=st.executeQuery(sql);
            
            int no =0;
            while(rs.next()){
                no++;
                modell.addRow(new Object[]{
                   no, rs.getString("id_pegawai"), rs.getString("nama_pegawai"),rs.getString("alamat"),
                    rs.getString("gender"),rs.getString("noHP"),rs.getString("password")
                });
            }
            tabelpegawai.setModel(modell);
        }catch(Exception e){
            System.out.println(e.getLocalizedMessage());
        }
    }
    
