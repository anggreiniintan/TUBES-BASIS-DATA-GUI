    private void tabelpegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpegawaiMouseClicked
        int baris = tabelpegawai.getSelectedRow();
        TableModel model = tabelpegawai.getModel();
        
        
        id.setText(tabelpegawai.getModel().getValueAt(baris, 1).toString()); 
        nama.setText(tabelpegawai.getModel().getValueAt(baris, 2).toString());
        alamat.setText(tabelpegawai.getModel().getValueAt(baris, 3).toString());
        
        String genderr = model.getValueAt(baris, 4).toString();
            switch(genderr){
                case "Wanita":
                    gender.setSelectedIndex(0);
                    break;
                    
                case "Laki-Laki":
                    gender.setSelectedIndex(1);
                    break;
               }
        hp.setText(tabelpegawai.getModel().getValueAt(baris, 5).toString()); 
        pw.setText(tabelpegawai.getModel().getValueAt(baris, 6).toString());


        // TODO add your handling code here:
    }//GEN-LAST:event_tabelpegawaiMouseClicked

